class Card:
    """
    花札の1枚を表現するクラス。
    """
    def __init__(self, month, type_, name):
        """
        :param month: 月 (1〜12)
        :param type_: カードの種類 ("光", "タネ", "短冊", "カス")
        :param name: カードの名前 (例: "松に鶴")
        """
        self.month = month
        self.type = type_
        self.name = name

    def __repr__(self):
        """
        カードの表示形式を定義。
        """
        return f"[{self.name} ({self.month}月, {self.type})]"


